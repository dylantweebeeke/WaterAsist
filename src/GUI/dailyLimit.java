package GUI;

public class dailyLimit {
    private int daglimietnr;
    private int datum;
    private int daglimiet;
    private int gebruikersnr;

    public int getDaglimietnr() {
        return daglimietnr;
    }

    public int getDatum() {
        return datum;
    }

    public int getDaglimiet() {
        return daglimiet;
    }

    public int getGebruikersnr() {
        return gebruikersnr;
    }

    public void setDatum(int datum) {
        this.datum = datum;
    }

    public void setDaglimiet(int daglimiet) {
        this.daglimiet = daglimiet;
    }

    public void setGebruikersnr(int gebruikersnr) {
        this.gebruikersnr = gebruikersnr;
    }
}

